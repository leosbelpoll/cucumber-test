package com.leito.calc;

public class Calc {
    public static long sumar(long num1, long num2) {
        return num1 + num2;
    }

    public static long restar(long num1, long num2) {
        return num1 - num2;
    }

    public static long multiplicar(long num1, long num2) {
        return num1 * num2;
    }

    public static long dividir(long num1, long num2) {
        return num1 / num2;
    }
}
