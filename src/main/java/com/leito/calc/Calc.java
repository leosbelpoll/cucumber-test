package com.leito.calc;

public class Calc {
    public static long sum(long num1, long num2) {
        return num1 + num2;
    }

    public static long rest(long num1, long num2) {
        return num1 - num2;
    }

    public static long divide(long num1, long num2) {
        return num1/num2;
    }

    public static long multiply(long num1, long num2) {
        return num1 * num2;
    }
}
